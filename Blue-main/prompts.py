# 课程名关键字 -> 学科类型（用于 --discipline auto）
_DISCIPLINE_KEYWORDS = {
    "cs": ["程序", "算法", "数据结构", "操作系统", "网络", "数据库", "编译",
           "机器学习", "深度学习", "计算机", "软件", "java", "python", "c++", "离散"],
    "science": ["数学", "物理", "化学", "力学", "电磁", "热学", "光学",
                "微积分", "线代", "线性代数", "概率", "统计", "工程"],
    "business": ["经济", "金融", "管理", "会计", "营销", "宏观", "微观",
                 "财政", "货币", "is-lm", "商"],
    "arts": ["史", "哲学", "文学", "法学", "社会", "政治学", "新闻", "教育"],
}


def detect_discipline(course_name: str) -> str:
    name = (course_name or "").lower()
    for kind, kws in _DISCIPLINE_KEYWORDS.items():
        if any(k in name for k in kws):
            return kind
    return "general"


def build_system_prompt(discipline: str = "general") -> str:
    extra = DISCIPLINE_EXTRAS.get(discipline, "")
    return SYSTEM_PROMPT + extra


def build_chunk_user_prompt(course, chapter, chunk_text, chunk_index, total_chunks,
                            known_concepts=None):
    """分片级 user prompt：强制完整读取 + 考试导向输出。"""
    known = ""
    if known_concepts:
        known = (
            "\n\n[已识别概念清单（来自前面的分片，命名请保持一致，指同一对象请沿用同名）]\n"
            + "、".join(known_concepts[:120])
        )
    cont = ""
    if total_chunks > 1:
        cont = f"\n（本资料已按章节切片，你当前处理的是【{chapter}】的第 {chunk_index}/{total_chunks} 片。）"
    return f"""课程：《{course}》
当前章节：{chapter}{cont}

【硬性约束】
1. 你看到的是从原始资料中逐页提取的文本（已标注来源 [文件名 P页码]），必须完整阅读，不得因长度截断或跳过。
2. 但"读全"不等于"全写"——输出要严格遵循系统提示词的考试优先原则：高频、真题、教师强调的内容详写，背景/沿革/拓展内容压缩。
3. 只整理本片可见的内容；不要臆造资料中没有的知识，但核心考点若缺少例题，可自编一道并在末尾用（模拟题）标注。
4. 真题、重点定义请保留来源 [文件名 P页码]，方便学生回溯。
5. 严格使用系统提示词规定的排版标记（【定义】/【高频】/【重点】/【必背】/【易错点】/【真题】/【记忆】/【解题步骤】/ 公式编号）。
6. 输出本章节的 Markdown 正文，不要重复课程级标题。

{known}

【本片原始资料】
{chunk_text}
"""


